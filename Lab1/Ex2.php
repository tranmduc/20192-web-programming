        <h2 style="text-align:center">Properties of Information</h2>
        <hr />
        <ul>
            <li><h3>Protection</h3></li>
            The supplier and the user of information often need special protection by means of government intervention such as copyright and patent laws, privacy legislation, fraud statutes, etc.

            <li><h3>Demand Varies</h3></li>
            Demand for information is a function of such variables as age, perishability, convenience, reliability, source, etc.

            <li><h3>Shareable</h3></li>
            It can be shared by many and can be used simultaneously without any loss to anyone.

            <li><h3>Democratic Resource</h3></li>
            Information is the best democratic resource that can be consumed by the poor and the rich alike depending upon their intake capacity.

            <li><h3>Un-destroyable</h3></li>
            Information is not consumed in its use.
            
